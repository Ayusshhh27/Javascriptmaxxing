// - **Definition**: JavaScript (JS) is a programming language used to give instructions to computers.
// - **Development Environment**: Initially, code can be written in the browser console for quick testing, later transitioning to using JS files in code editors like VS Code.

// - **Types**:
//   - `var`: Can be re-declared and updated, with a global scope.
//   - `let`: Cannot be re-declared but can be updated, with a block scope.
//   - `const`: Cannot be re-declared or updated, with a block scope.

// **Data Types in JS:**

// - **Primitive Types**: Number, String, Boolean, Undefined, Null, BigInt, Symbol.

